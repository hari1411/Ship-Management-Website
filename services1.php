                        <p style="font-size: 120%;">
                        ASM is experienced in Technical ship management which is a critical division of ship management services. Ship management encompasses a wide range of services that are highly useful to ship owners.<br>The complete range of technical ship management services can be divided into seven main categories. These are Budgeting, Maintenance, Inspections and Vetting, Drydocking, Procurement, Safety management system (SMS), and  Documentation.
                        <br>Many business owners decide to own or charter ships to ensure fast and efficient movement of goods over large distances. However, they don’t necessarily have the technical expertise to manage them.<br>
                        In such a situation, they choose the logical solution of delegating the management of a vessel to ship management companies. Ship management companies have a boatload of experience in managing different types of vessels run by diverse crews.They cannot only manage the vessels well but also increase their productivity by reducing the number of incidents that impede a ship’s operations.
                        <p>1) Budgeting -
                        Budgeting is the judicious use of resources to reduce the operating costs and keep them within the allotted budget for the vessel. Having a large fleet under their care allows ship management companies to work on economies of scale and secure commission rebates for their client, keeping the operating costs well within the permitted limit.
                        </p>
                        <p>2) Maintenance -
                        Maintenance refers to the efficient execution of predictive, preventive and breakdown maintenance to ensure a long service life for all the equipment onboard. Ship management companies can ensure a timely maintenance schedule that extends equipment life by ensuring the ship receives all the right spares, stores and bunkers.<br>

                        To this end, almost all ship management companies use a PMS (Planned maintenance system) software to keep track of the hundreds of maintenance jobs that are due every month.
                        </p>
                        <p>
                            3) Inspections and Vetting -
                            Inspections are carried out by various organizations such as flag states, classification societies, port state control and ship managers to ensure that the ship complies with various international and local regulations. Failing inspections can lead to detention, fines and even prison time for the crew onboard.

                        Vetting inspections are special types of inspections that are carried out at the request of ship owners. The vetting inspector gives the vessel a score that helps owners pitch their vessel for cargo through charterers. This is because charterers depend on vetting scores to determine the suitability of a vessel to carry their cargo when there are multiple similar vessels available.

                        Vetting inspections are carried out by various organizations such as OCIMF (SIRE), Rightship, CDI and P&I clubs.

                                                </p>
                                                <p>
                                                    4) Drydocking
                        SOLAS requirements state that all merchant vessels must undergo a hull survey at a drydock at least twice in five years and an intermediate survey within the first 36 months of those five years.

                        A successful dry dock requires several months of planning and must consider factors such as budget, repair needs and vessel size, age and condition. The objective is to carry out all the necessary repairs at an optimum cost. Ship management companies can ensure systematic planning and smooth execution of the drydock plan while staying within the allotted budget.

                        Other services provided by a ship management company include certification, performance monitoring, emergency contingency, energy efficiency and comprehensive reporting.

                                                </p>
                                                <p>
                                                    5) Procurement
                        Procurement is the purchase and supply of items requested by the ship staff. To run a ship without any delays, it must be done in time while staying within budget and maintaining the recommended quality.

                        The logistics can be quite a challenge when heavy or restricted items (batteries, medical equipment, etc.) must be transported across large distances within time while complying with local and international regulations.

                        It also includes the supply of various technical consumables such as fasteners (bolts, nuts, washers), lumber products, welding rods, steel brushes, gases, cutting and grinding discs, etc. within time.
                                                </p>
                                                <p>
                                                    6) Safety management system (SMS)
                        Safety is of utmost concern on ships. Accidents, incidents and near misses put the life, vessel and environment at risk. As a result, all ships try to maintain a zero incident policy onboard their vessels.

                        A well-established ship management company with an experienced team can provide the shipboard team with an effective safety management system (SMS).

                        The International safety management code (ISM), issued by the IMO describes SMS as “a structured and documented system enabling the company personnel to implement effectively the Company Safety and Environmental protection policy.”

                        Choosing the right ship management company can provide the owners with a dependable SMS that eliminates safety incidents and minimizes operational hiccups providing increased productivity.
                                                </p>
                                                <p>
                                                    7. Documentation
                        Maintaining updated documentation on board is a necessity to prevent delays and detentions. A ship requires many approvals and relevant certificates from the flag state, classification society, insurance, MLC, and so on.

                        To ply in specific countries such as USA or Australia, a vessel may require further approvals from local authorities such as the USCG and AMSA.

                        Besides vessels, critical safety, operational and pollution prevention equipment such as EPIRB, SART, voyage data recorder, oily water separator, etc. must have approval and regular servicing certificates. Even if a piece of equipment is in pristine condition, it has no standing without a validity certificate.

                        Managing the complete set of certifications can be a regulatory and logistical challenge and an inefficient execution can cause unavoidable delays, detentions, additional port charges and huge fines.

                        The supervising teams at ship management companies can prevent this from happening by keeping a close eye on the ship certificates. They understand the lead time to obtain different approvals which allows them to apply in time and ensure the validity of all certificates required to operate in a certain region.
                                                </p>
